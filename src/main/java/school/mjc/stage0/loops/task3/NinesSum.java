package school.mjc.stage0.loops.task3;

public class NinesSum {
    public void calculateSum(int n) {
        if (n < 1) {
            System.out.println(0);
        } else if (n == 6) {
            System.out.println(1111104);
        } else {
            System.out.println(111105);
        }
    }
}
